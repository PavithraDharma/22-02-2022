package com.demo;

public class SingledimArraymultipleline {
	// java program single dimensional array in multiple lines
	public static void main(String[] args) {
		int empid[]=new int[3];
		empid[0]=0;
		empid[1]=1;
		empid[2]=2;
		for(int id:empid)
		{
			System.out.println(id);
		}
	}
}

